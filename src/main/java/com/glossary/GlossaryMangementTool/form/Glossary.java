package com.glossary.GlossaryMangementTool.form;

public class Glossary {

	private int glossaryId;

	private String japaneseReading;

	private String japaneseTerm;

	private String englishTerm;

	private String japaneseDescription;

	public int getGlossaryId() {
		return glossaryId;
	}

	public void setGlossaryId(int glossaryId) {
		this.glossaryId = glossaryId;
	}

	public String getJapaneseReading() {
		return japaneseReading;
	}

	public void setJapaneseReading(String japaneseReading) {
		this.japaneseReading = japaneseReading;
	}

	public String getJapaneseTerm() {
		return japaneseTerm;
	}

	public void setJapaneseTerm(String japaneseTerm) {
		this.japaneseTerm = japaneseTerm;
	}

	public String getEnglishTerm() {
		return englishTerm;
	}

	public void setEnglishTerm(String englishTerm) {
		this.englishTerm = englishTerm;
	}

	public String getJapaneseDescription() {
		return japaneseDescription;
	}

	public void setJapaneseDescription(String japaneseDescription) {
		this.japaneseDescription = japaneseDescription;
	}

}
